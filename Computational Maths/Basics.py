#Numbers
x=11
y=2

#Addition
add=x+y
#Subtraction
sub=x-y
#Multiplication
mul=x*y
#Division(float)
divft=x/y
#Division(floor)
divfr=x//y
#Division(Modular)
divmr=x%y
#Power
pow=x**y

#Print results
print("Addition=", add)
print("Subtraction=", sub)
print("Multiplication=", mul)
print("Division(float)=", divft)
print("Division(floor)=", divfr)
print("Division(Modular)=", divmr)
print("Power=", pow)


